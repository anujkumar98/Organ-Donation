    private void populateTable(ArrayList<PatientVisit> patientVisits) {
        DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for (PatientVisit v: patientVisits)
        {
            Object[] row =new Object[7];
            row[0]=v.getId();
            row[1]=v.getName();
            row[2]=v.getType();
            row[3]=v.getDate();
            row[4]=v.getOrganStatus();
            model.addRow(row);
        }
    }

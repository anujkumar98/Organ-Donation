    void populateTable(TransportDirectory td){
        ArrayList<TransportEnterprise> transportDirectory=td.getTransportDirectory();
        DefaultTableModel model=(DefaultTableModel) jTableTransport.getModel();
        model.setRowCount(0);
        for (TransportEnterprise t: transportDirectory)
        {
            Object[] row =new Object[7];
            row[0]=t.getId();
            row[1]=t.getName();
            row[2]=t.getRegion();
            row[3]=t.getCity();
            row[4]=t.getState();
            if (t.getAdminName() == null){
                row[5]="No Admin";
            }else{
            row[5]=t.getAdminName();
            }
            model.addRow(row);
        }
    }
    
     private void addDropdown(ArrayList <Network> networkList) {
        ArrayList<String> state = new ArrayList();
        ArrayList<String> city = new ArrayList();
        ArrayList<String> region = new ArrayList();
        for (Network n: networkList){
            state.add(n.getState());
            city.add(n.getCity());
            region.add(n.getRegion());
        }
        state = (ArrayList<String>) state.stream().distinct().collect(Collectors.toList());
        city=(ArrayList<String>) city.stream().distinct().collect(Collectors.toList());
        region=(ArrayList<String>) region.stream().distinct().collect(Collectors.toList());
        for (int i=0;i<city.size();i++){
            jComboCity.addItem(city.get(i));
        }
        for (int i=0;i<state.size();i++){
            jComboBoxState.addItem(state.get(i));
        }
        for (int i=0;i<region.size();i++){
            jComboBoxRegion.addItem(region.get(i));
        }
    }

